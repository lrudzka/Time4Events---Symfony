    /**
     * 
     * @return array
     */
    public function findCategories() {
        return $this
                ->getEntityManager()
                ->createQuery(
                    "SELECT c.id id, c.name name, count(e.id) liczba FROM App:Category c
                     LEFT JOIN App:Event e WITH c.name = e.category
                     GROUP BY c.name
                     ORDER BY liczba DESC
                     "
                    )
                ->getResult();
    }
